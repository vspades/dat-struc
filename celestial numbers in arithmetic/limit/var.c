#include <stdio.h>

void main() {
	printf("integer:\t\t%i\n", 32);
	printf("size of (integer):\t%lu\n", sizeof(int));
	printf("\n");
	printf("float:\t\t\t%f\n", 3.2);
	printf("size of (float):\t%lu\n", sizeof(float));
	printf("\n");
	printf("char:\t\t\t%c\n", 'c');
	printf("size of (char):\t\t%lu\n", sizeof(char));
	printf("\n");
}
